package FirstDay;

public class float_study {

		public static void main(String[] args) {
			// 실수형 data type
			float a=10.1f;	// 1.40129846432481707e-45 ~ 3.40282346638528860e+38
			double b=10.1f; //근사값
			double c=10.1d;	//4.94065645841246544e-324d ~ 1.79769313486231570e+308d
			double d=10.1;	// 근사값
			System.out.println(a);
			System.out.println(b);
			System.out.println(c);
			System.out.println(d);
		}
}
