package FirstDay;

public class int_study {

		public static void main(String[] args) {
				// 정수형 data type
				byte a= 10;	// -128 ~ 127
				short b= 10;	// -32768 ~ 32767
				int c=10;	// -2,147,483,648 ~ 2,147,483,647
				long d=10;	// -9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807
				System.out.println(a);
				System.out.println(b);
				System.out.println(c);
				System.out.println(d);
		}
}
